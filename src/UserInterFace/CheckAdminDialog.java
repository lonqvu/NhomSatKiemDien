    private boolean CheckNull() {
        boolean kq = true;
        if (String.valueOf(this.passWord.getPassword()).length() == 0) {
            JOptionPane.showMessageDialog(null, "Bạn chưa nhập mật khẩu!", "Thông báo", JOptionPane.WARNING_MESSAGE);
            return false;
        }
        return kq;
    }


