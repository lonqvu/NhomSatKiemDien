    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        String sql = "SELECT * FROM Accounts WHERE UserName=? and PassWord=?";
        if (CheckNull())
        try {
            pst = conn.prepareStatement(sql);
            pst.setString(1, this.userName.getText());
            pst.setString(2, String.valueOf(this.passWord.getPassword()));
            rs = pst.executeQuery();
            if (rs.next()) {
                if (rs.getString("RoleID").equals("1")) {
                    isLogin = true;
                    this.dispose();
                } else {
                    isLogin = false;
                    JOptionPane.showMessageDialog(null, "Tài khoản không có quyền admin", "Thông báo", JOptionPane.WARNING_MESSAGE);
                }
            } else {
                JOptionPane.showMessageDialog(null, "Mật khẩu không hợp lệ", "Thông báo", JOptionPane.WARNING_MESSAGE);
            }
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }//GEN-LAST:event_btnLoginActionPerformed

